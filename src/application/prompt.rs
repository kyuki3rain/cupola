use crate::domain::config::Config;
use crate::domain::state::State;

pub struct SessionConfig {
    pub prompt: String,
    pub output_schema: OutputSchemaKind,
}

#[derive(Debug, Clone, Copy, PartialEq, Eq)]
pub enum OutputSchemaKind {
    PrCreation,
    Fixing,
}

pub const PR_CREATION_SCHEMA: &str = r#"{"type":"object","properties":{"pr_title":{"type":"string","description":"PR のタイトル"},"pr_body":{"type":"string","description":"PR の body（Markdown 形式）"}},"required":["pr_title","pr_body"]}"#;

pub const FIXING_SCHEMA: &str = r#"{"type":"object","properties":{"threads":{"type":"array","items":{"type":"object","properties":{"thread_id":{"type":"string","description":"対応した review thread の ID（PRRT_...）"},"response":{"type":"string","description":"review thread への返信内容"},"resolved":{"type":"boolean","description":"この thread を resolve するか"}},"required":["thread_id","response","resolved"]}}},"required":["threads"]}"#;

pub fn build_session_config(
    state: State,
    issue_number: u64,
    config: &Config,
    pr_number: Option<u64>,
) -> SessionConfig {
    match state {
        State::DesignRunning => SessionConfig {
            prompt: build_design_prompt(issue_number, &config.language),
            output_schema: OutputSchemaKind::PrCreation,
        },
        State::DesignFixing => {
            let pr = pr_number.expect("fixing state requires pr_number in DB");
            SessionConfig {
                prompt: build_fixing_prompt(issue_number, pr, &config.language),
                output_schema: OutputSchemaKind::Fixing,
            }
        }
        State::ImplementationRunning => SessionConfig {
            prompt: build_implementation_prompt(issue_number, &config.language),
            output_schema: OutputSchemaKind::PrCreation,
        },
        State::ImplementationFixing => {
            let pr = pr_number.expect("fixing state requires pr_number in DB");
            SessionConfig {
                prompt: build_fixing_prompt(issue_number, pr, &config.language),
                output_schema: OutputSchemaKind::Fixing,
            }
        }
        _ => unreachable!("build_session_config called for non-process state: {state:?}"),
    }
}

pub fn fallback_pr_title(state: State, issue_number: u64) -> String {
    match state {
        State::DesignRunning => format!("Design: Issue #{issue_number}"),
        State::ImplementationRunning => format!("Implement: Issue #{issue_number}"),
        _ => format!("Issue #{issue_number}"),
    }
}

pub fn fallback_pr_body(state: State, issue_number: u64) -> String {
    let relation = match state {
        State::DesignRunning => format!("Related: #{issue_number}"),
        State::ImplementationRunning => format!("Closes #{issue_number}"),
        _ => format!("Related: #{issue_number}"),
    };
    format!("{relation}\n\n---\n🤖 Generated by Cupola via cc-sdd")
}

fn build_design_prompt(issue_number: u64, language: &str) -> String {
    format!(
        r#"あなたは自動設計エージェントです。Issue の内容に基づき、cc-sdd による設計一式を生成してください。

## 入力

Issue の内容は .cupola/inputs/issue.md を参照してください。

## 手順

1. .cupola/inputs/issue.md から Issue の内容を読み取る

2. cc-sdd でスペックを初期化する
   /kiro:spec-init に Issue の概要を渡して実行
   （spec-init が生成する feature name を以降のコマンドで使用すること）

3. 要件定義を生成する
   /kiro:spec-requirements <feature_name>
   Issue 本文の内容を入力として使用すること

4. 設計書を生成する（自動承認）
   /kiro:spec-design <feature_name> -y

5. タスクを生成する（自動承認）
   /kiro:spec-tasks <feature_name> -y

6. 成果物を commit / push する
   git add .cupola/specs/ .cupola/steering/
   git commit -m "docs: add cc-sdd design for #{issue_number}"
   git push

## output-schema への出力

PR の作成はシステム側で行います。以下の情報を出力してください。

- pr_title: 設計 PR のタイトル。"Design: <Issue の要約>" の形式
- pr_body: 設計 PR の body。{language} で記述すること

## 制約事項

- 成果物はすべて {language} で記述すること
- GitHub API（gh コマンド含む）は使用しないこと
- PR の作成は行わないこと（システム側で行います）"#
    )
}

fn build_implementation_prompt(issue_number: u64, language: &str) -> String {
    format!(
        r#"あなたは自動実装エージェントです。設計書に基づき、cc-sdd による実装を行ってください。

## 前提

- 現在のブランチに設計成果物が統合されています
- .cupola/specs/ 配下にスペック（requirements.md, design.md, tasks.md）が存在します

## 手順

1. スペックの feature name を確認する
   ls .cupola/specs/ で feature ディレクトリを特定

2. cc-sdd による実装を実行する
   /kiro:spec-impl <feature_name>
   タスクを TDD で順次実行すること

3. 成果物を commit / push する
   最終的に git push

## output-schema への出力

PR の作成はシステム側で行います。以下の情報を出力してください。

- pr_title: 実装 PR のタイトル。"Implement: <Issue の要約>" の形式
- pr_body: 実装 PR の body。{language} で記述すること。Closes #{issue_number} を含めること

## 制約事項

- GitHub API（gh コマンド含む）は使用しないこと
- PR の作成は行わないこと（システム側で行います）"#
    )
}

fn build_fixing_prompt(_issue_number: u64, _pr_number: u64, language: &str) -> String {
    format!(
        r#"あなたはレビュー対応エージェントです。レビュー指摘に対応してください。

## 入力

レビュー指摘内容は .cupola/inputs/review_threads.json を参照してください。
各エントリには thread_id、対象ファイルのパス・行番号、レビュアーのコメント履歴が含まれています。

## 手順

1. .cupola/inputs/review_threads.json を読み取る

2. 各 thread に対して以下を行う
   a. レビューコメントの内容を理解する
   b. 必要な修正をコードに反映する
   c. 対応不要と判断した場合はその理由を明確にする

3. 修正を commit / push する
   git add -A
   git commit -m "fix: address review comments"
   git push

## output-schema への出力

コメントの返信と thread の resolve はシステム側で行います。
各 thread への対応結果を出力してください。

- thread_id: 対応した thread の ID（review_threads.json の thread_id をそのまま使用）
- response: その thread への返信内容（{language} で記述）
- resolved: この thread を resolve してよいか（true/false）

## 制約事項

- GitHub API（gh コマンド含む）は使用しないこと
- コメントの返信や thread の resolve は行わないこと（システム側で行います）
- 返信内容はすべて {language} で記述すること"#
    )
}

#[cfg(test)]
mod tests {
    use super::*;

    fn test_config() -> Config {
        Config::default_with_repo("owner".into(), "repo".into(), "main".into())
    }

    #[test]
    fn design_running_returns_pr_creation_schema() {
        let config = test_config();
        let session = build_session_config(State::DesignRunning, 42, &config, None);
        assert_eq!(session.output_schema, OutputSchemaKind::PrCreation);
        assert!(session.prompt.contains("自動設計エージェント"));
        assert!(session.prompt.contains("#42"));
    }

    #[test]
    fn implementation_running_returns_pr_creation_schema() {
        let config = test_config();
        let session = build_session_config(State::ImplementationRunning, 42, &config, None);
        assert_eq!(session.output_schema, OutputSchemaKind::PrCreation);
        assert!(session.prompt.contains("自動実装エージェント"));
    }

    #[test]
    fn design_fixing_returns_fixing_schema() {
        let config = test_config();
        let session = build_session_config(State::DesignFixing, 42, &config, Some(85));
        assert_eq!(session.output_schema, OutputSchemaKind::Fixing);
        assert!(session.prompt.contains("レビュー対応エージェント"));
    }

    #[test]
    fn implementation_fixing_returns_fixing_schema() {
        let config = test_config();
        let session = build_session_config(State::ImplementationFixing, 42, &config, Some(90));
        assert_eq!(session.output_schema, OutputSchemaKind::Fixing);
    }

    #[test]
    fn fallback_pr_title_for_design() {
        assert_eq!(
            fallback_pr_title(State::DesignRunning, 42),
            "Design: Issue #42"
        );
    }

    #[test]
    fn fallback_pr_title_for_implementation() {
        assert_eq!(
            fallback_pr_title(State::ImplementationRunning, 42),
            "Implement: Issue #42"
        );
    }

    #[test]
    fn fallback_pr_body_for_design() {
        let body = fallback_pr_body(State::DesignRunning, 42);
        assert!(body.contains("Related: #42"));
        assert!(body.contains("Cupola"));
    }

    #[test]
    fn fallback_pr_body_for_implementation() {
        let body = fallback_pr_body(State::ImplementationRunning, 42);
        assert!(body.contains("Closes #42"));
    }

    #[test]
    fn pr_creation_schema_is_valid_json() {
        let _: serde_json::Value =
            serde_json::from_str(PR_CREATION_SCHEMA).expect("should be valid JSON");
    }

    #[test]
    fn fixing_schema_is_valid_json() {
        let _: serde_json::Value =
            serde_json::from_str(FIXING_SCHEMA).expect("should be valid JSON");
    }
}

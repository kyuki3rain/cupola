use crate::domain::state::State;

#[derive(Debug)]
pub struct SessionConfig {
    pub prompt: String,
    pub output_schema: OutputSchemaKind,
}

#[derive(Debug, Clone, Copy, PartialEq, Eq)]
pub enum OutputSchemaKind {
    PrCreation,
    Fixing,
}

pub const PR_CREATION_SCHEMA: &str = r#"{"type":"object","properties":{"pr_title":{"type":"string","description":"Title of the PR"},"pr_body":{"type":"string","description":"Body of the PR (Markdown format)"},"feature_name":{"type":"string","description":"Feature name of cc-sdd (directory name under .cupola/specs/)"}},"required":["pr_title","pr_body"]}"#;

pub const FIXING_SCHEMA: &str = r#"{"type":"object","properties":{"threads":{"type":"array","items":{"type":"object","properties":{"thread_id":{"type":"string","description":"ID of the review thread addressed (PRRT_...)"},"response":{"type":"string","description":"Reply content to the review thread"},"resolved":{"type":"boolean","description":"Whether to resolve this thread"}},"required":["thread_id","response","resolved"]}}},"required":["threads"]}"#;

pub const GENERIC_QUALITY_CHECK_INSTRUCTION: &str = "Run quality checks described in AGENTS.md / CLAUDE.md before committing. If any check fails, fix the issues and re-run.";

pub fn build_session_config(
    state: State,
    issue_number: u64,
    config: &Config,
    pr_number: Option<u64>,
    feature_name: Option<&str>,
    fixing_causes: &[FixingProblemKind],
    has_merge_conflict: bool,
) -> anyhow::Result<SessionConfig> {
    match state {
        State::DesignRunning => Ok(SessionConfig {
            prompt: build_design_prompt(issue_number, &config.language),
            output_schema: OutputSchemaKind::PrCreation,
        }),
        State::DesignFixing => {
            let pr = pr_number.context("fixing state requires pr_number in DB")?;
            Ok(SessionConfig {
                prompt: build_fixing_prompt(
                    issue_number,
                    pr,
                    &config.language,
                    fixing_causes,
                    has_merge_conflict,
                ),
                output_schema: OutputSchemaKind::Fixing,
            })
        }
        State::ImplementationRunning => Ok(SessionConfig {
            prompt: build_implementation_prompt(issue_number, &config.language, feature_name),
            output_schema: OutputSchemaKind::PrCreation,
        }),
        State::ImplementationFixing => {
            let pr = pr_number.context("fixing state requires pr_number in DB")?;
            Ok(SessionConfig {
                prompt: build_fixing_prompt(
                    issue_number,
                    pr,
                    &config.language,
                    fixing_causes,
                    has_merge_conflict,
                ),
                output_schema: OutputSchemaKind::Fixing,
            })
        }
        _ => unreachable!("build_session_config called for non-process state: {state:?}"),
    }
}

pub fn fallback_pr_title(state: State, issue_number: u64) -> String {
    match state {
        State::DesignRunning => format!("Design: Issue #{issue_number}"),
        State::ImplementationRunning => format!("Implement: Issue #{issue_number}"),
        _ => format!("Issue #{issue_number}"),
    }
}

pub fn fallback_pr_body(state: State, issue_number: u64) -> String {
    let relation = match state {
        State::DesignRunning => format!("Related: #{issue_number}"),
        State::ImplementationRunning => format!("Closes #{issue_number}"),
        _ => format!("Related: #{issue_number}"),
    };
    format!("{relation}\n\n---\n🤖 Generated by Cupola via cc-sdd")
}

fn build_design_prompt(issue_number: u64, language: &str) -> String {
    format!(
        r#"You are an automated design agent. Generate a complete cc-sdd design based on the Issue content.

## Input

Refer to .cupola/inputs/issue.md for the Issue content.

## Steps

1. Read the Issue content from .cupola/inputs/issue.md

2. Initialize the spec with cc-sdd
   Run /kiro:spec-init with a summary of the Issue
   (Use the feature name generated by spec-init in subsequent commands)

3. Generate requirements
   /kiro:spec-requirements <feature_name>
   Use the Issue body as input

4. Generate design (auto-approve)
   /kiro:spec-design <feature_name> -y

5. Generate tasks (auto-approve)
   /kiro:spec-tasks <feature_name> -y

6. {quality_check}

7. Commit and push the artifacts
   git add .cupola/specs/ .cupola/steering/
   git commit -m "docs: add cc-sdd design for #{issue_number}"
   git push

## Output to output-schema

The PR will be created by the system. Please output the following information.

- pr_title: Title of the design PR. Format: "Design: <Issue summary>"
- pr_body: Body of the design PR. Write in {language}. Include Related: #{issue_number}
- feature_name: The cc-sdd feature name (directory name under .cupola/specs/ generated by spec-init)

## Constraints

- Write all artifacts in {language}
- Do not use the GitHub API (including the gh command)
- Do not create a PR (the system will do it)
- Do not use auto-closing keywords in the PR body (the design PR is an intermediate artifact)"#,
        quality_check = GENERIC_QUALITY_CHECK_INSTRUCTION,
    )
}

fn build_implementation_prompt(
    issue_number: u64,
    language: &str,
    feature_name: Option<&str>,
) -> String {
    let feature_instruction = match feature_name {
        Some(name) => format!(
            "1. Run the implementation with cc-sdd\n   /kiro:spec-impl {name}\n   Execute tasks sequentially using TDD"
        ),
        None => "1. Identify the spec feature name\n   Run ls .cupola/specs/ to find the feature directory\n   (If multiple exist, select the one with phase \"tasks-generated\" in spec.json)\n\n2. Run the implementation with cc-sdd\n   /kiro:spec-impl <feature_name>\n   Execute tasks sequentially using TDD".to_string(),
    };

    let (quality_check_step, push_step) = if feature_name.is_some() {
        ("2", "3")
    } else {
        ("3", "4")
    };

    format!(
        r#"You are an automated implementation agent. Implement using cc-sdd based on the design documents.

## Prerequisites

- The design artifacts have been merged into the current branch
- The spec (requirements.md, design.md, tasks.md) exists under .cupola/specs/

## Steps

{feature_instruction}

{quality_check_step}. {quality_check}

{push_step}. Commit and push the artifacts
   Finally run git push

## Output to output-schema

The PR will be created by the system. Please output the following information.

- pr_title: Title of the implementation PR. Format: "Implement: <Issue summary>"
- pr_body: Body of the implementation PR. Write in {language}. Include Closes #{issue_number}

## Constraints

- Do not use the GitHub API (including the gh command)
- Do not create a PR (the system will do it)"#,
        quality_check = GENERIC_QUALITY_CHECK_INSTRUCTION,
    )
}

fn build_fixing_prompt(
    issue_number: u64,
    pr_number: u64,
    language: &str,
    causes: &[FixingProblemKind],
    has_merge_conflict: bool,
) -> String {
    let conflict_section = if has_merge_conflict {
        r#"## Merge Conflict Resolution Required

The working tree is in the middle of a merge.
Conflict markers (<<<<<<<, =======, >>>>>>>) remain in the files.

1. Resolve all conflicts first
2. Stage resolved files with `git add <resolved_files>`
3. Complete the merge with `git commit --no-edit`
4. Then proceed with the other fixes

"#
    } else {
        ""
    };

    let mut instructions = Vec::new();

    if causes.contains(&FixingProblemKind::ReviewComments) {
        instructions.push(
            "Refer to .cupola/inputs/review_threads.json and address the review comments."
                .to_string(),
        );
    }

    if causes.contains(&FixingProblemKind::CiFailure) {
        instructions
            .push("Refer to .cupola/inputs/ci_errors.txt and fix the CI failures.".to_string());
    }

    if causes.contains(&FixingProblemKind::Conflict) {
        instructions.push("Resolve conflicts with the base branch.".to_string());
    }

    let instructions_text = if instructions.is_empty() {
        "Review the feedback and apply the necessary fixes.".to_string()
    } else {
        instructions
            .iter()
            .enumerate()
            .map(|(i, s)| format!("{}. {s}", i + 1))
            .collect::<Vec<_>>()
            .join("\n")
    };

    let output_section = if causes.contains(&FixingProblemKind::ReviewComments) {
        format!(
            r#"Comment replies and thread resolution will be handled by the system.
If you addressed any threads, output the result for each thread.

- thread_id: The ID of the thread addressed (use thread_id as-is)
- response: Reply content for that thread (write in {language})
- resolved: Whether this thread should be resolved (true/false)"#
        )
    } else {
        r#"No review thread responses are required for this fix.
Return the following as output:

{"threads": []}"#
            .to_string()
    };

    format!(
        r#"{conflict_section}You are an automated review agent. Address the following issues.

PR #{pr_number} (Issue #{issue_number})

## Actions Required

{instructions_text}

## Steps

1. Review the issues listed above

2. Apply the necessary fixes to the code

3. {quality_check}

4. Commit and push the fixes
   Run `git diff --name-only` to confirm the changed files, then stage only the relevant files.
   Avoid staging temporary files or debug logs.
   git add <changed_files>
   git commit -m "fix: address requested changes"
   git push

## Output to output-schema

{output_section}

## Constraints

- Do not use the GitHub API (including the gh command)
- Do not reply to comments or resolve threads (the system will do it)
- Write all replies in {language}"#,
        quality_check = GENERIC_QUALITY_CHECK_INSTRUCTION,
    )
}

